export function useAiModelFindMany<
  T extends Prisma.ai_modelFindManyArgs,
  R extends GetFindResult<Prisma.$ai_modelPayload<DefaultArgs>, T>[],
>(
  args?: Prisma.SelectSubset<T, Prisma.ai_modelFindManyArgs>,
  options?: RequestOptions,
  swrOptions?: SWRRequestOptions<R>,
) {
  const roq = useRoqClient();
  const key = JSON.stringify(['useAiModelFindMany', args || {}]);
  return useSWR<R, Error>(
    key,
    async () => {
      const result = await roq.ai_model.findMany(args, options);
      return result as R;
    },
    {
      ...swrOptions,
      fallbackData: swrOptions?.initialData ?? swrOptions?.fallbackData,
    },
  );
}

